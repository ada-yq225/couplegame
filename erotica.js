function fillEroticaText(text, selfName, otherName, nameA, nameB) {
  if (!text) return "";
  return text
    .replace(/\{self\}/g, selfName)
    .replace(/\{other\}/g, otherName)
    .replace(/\{A\}/g, nameA || "")
    .replace(/\{B\}/g, nameB || "");
}

function formatEroticaNarrative(text) {
  return text
    .split(/\n\n+/)
    .map((p) => p.trim())
    .filter(Boolean)
    .map((p) => `<p>${p}</p>`)
    .join("");
}

