function getHeroine(id) {
  return GAL_HEROINES[id] || null;
}

function getActiveHeroine() {
  const id = getGalCampusMeta()?.activeHeroine || galRuntime.heroineId;
  return id ? getHeroine(id) : null;
}

function getGalCharName() {
  return getActiveHeroine()?.name || state.nameA || "她";
}

function getBodyFlavor(bodyType, kind) {
  const pool = GAL_BODY_FLAVOR[bodyType]?.[kind] || GAL_BODY_FLAVOR.slim[kind];
  return galPick(pool);
}

function getPersonaLine(personality, key) {
  const pool = GAL_PERSONA_DIALOGUE[personality]?.[key];
  if (!pool?.length) return "";
  return galFill(galPick(pool));
}

function getPersonaOutfitDesc(heroine, outfitId) {
  const p = heroine?.personality || "cool";
  const desc = GAL_PERSONA_DIALOGUE[p]?.outfit?.[outfitId];
  return desc ? galFill(desc) : "";
}

function getPersonaSexLine(heroine, pace) {
  const p = heroine?.personality || "cool";
  const key = pace === "climax" ? "sex_climax" : `sex_${pace}`;
  const pool = GAL_PERSONA_DIALOGUE[p]?.[key];
  if (!pool?.length) return { narrative: "", char: "" };
  const line = galPick(pool);
  if (typeof line === "string") return { narrative: "", char: galFill(line) };
  return { narrative: galFill(line.narrative || ""), char: galFill(line.char || line) };
}

