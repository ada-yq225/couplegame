function mapEroticaStageIndex(sectionIndex, totalSections) {
  if (totalSections <= 1) return 2;
  const ratio = sectionIndex / (totalSections - 1);
  if (ratio < 0.15) return 0;
  if (ratio < 0.35) return 1;
  if (ratio < 0.55) return 2;
  if (ratio < 0.8) return 3;
  return 4;
}

function fillPlayText(text, self, other, nameA, nameB) {
  if (!text) return "";
  return text
    .replace(/\{self\}/g, self)
    .replace(/\{other\}/g, other)
    .replace(/\{A\}/g, nameA || "")
    .replace(/\{B\}/g, nameB || "");
}

