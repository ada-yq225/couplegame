function mapEroticaStageIndex(sectionIndex, totalSections) {
  if (totalSections <= 1) return 0;
  const ratio = sectionIndex / (totalSections - 1);
  if (ratio < 0.2) return 0;
  if (ratio < 0.45) return 1;
  if (ratio < 0.7) return 2;
  if (ratio < 0.9) return 3;
  return 4;
}

function fillPlayText(text, self, other, nameA, nameB) {
  if (!text) return "";
  return text
    .replace(/\{self\}/g, self)
    .replace(/\{other\}/g, other)
    .replace(/\{A\}/g, nameA || "")
    .replace(/\{B\}/g, nameB || "");
}

function getEroticaPlayPrompt(story, sectionIndex, self, other, nameA, nameB, variant) {
  const scenePool = EROTICA_SCENE_PLAY[story?.id];
  if (scenePool?.[sectionIndex]) {
    const line = scenePool[sectionIndex];
    return fillPlayText(line, self, other, nameA, nameB);
  }
  const stageIdx = mapEroticaStageIndex(sectionIndex, story?.sections?.length || 5);
  const stage = EROTICA_PLAY_STAGES[stageIdx] || EROTICA_PLAY_STAGES[0];
  const pool = stage.pool;
  const i = variant != null ? variant % pool.length : Math.floor(Math.random() * pool.length);
  return fillPlayText(pool[i], self, other, nameA, nameB);
}

function getEroticaStageLabel(sectionIndex, totalSections) {
  const idx = mapEroticaStageIndex(sectionIndex, totalSections || 5);
  return EROTICA_PLAY_STAGES[idx]?.label || "欲起";
}

function drawFantasyCard(intensity, self, other, nameA, nameB) {
  const pool = FANTASY_DRAW_CARDS[intensity] || FANTASY_DRAW_CARDS.hot;
  const line = pool[Math.floor(Math.random() * pool.length)];
  return fillPlayText(line, self, other, nameA, nameB);
}

function getEroticaIntensityForSection(sectionIndex, total) {
  const idx = mapEroticaStageIndex(sectionIndex, total || 5);
  return ["warm", "warm", "hot", "deep", "deep"][idx] || "hot";
}

